                    <!-- wp:heading {"level":4} -->
                    <h4 class="wp-block-heading" id="strong-tsunami-alert-levels-strong-nbsp"><strong>Tsunami alert levels:</strong>&nbsp;</h4>
                    <!-- /wp:heading -->

                    <!-- wp:paragraph -->
                    <p><strong>Warning:</strong>&nbsp;</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p>The highest level of tsunami alert. Warnings are issued when there is an imminent threat of a tsunami from:&nbsp;</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:list -->
                    <ul class="wp-block-list"><!-- wp:list-item -->
                        <li>A large undersea earthquake, or&nbsp;</li>
                        <!-- /wp:list-item -->

                        <!-- wp:list-item -->
                        <li>Following confirmation that that a potentially destructive tsunami is underway&nbsp;</li>
                        <!-- /wp:list-item -->
                    </ul>
                    <!-- /wp:list -->

                    <!-- wp:paragraph -->
                    <p>At first, a Warning may be based on seismic information as a way of notifying the public early. Warnings advise that appropriate actions be taken in response to the tsunami threat. These actions could include the evacuation of low-lying coastal areas.&nbsp;</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p><strong>Advisory:</strong>&nbsp;</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p>The second highest level of tsunami alert. Advisories are issued due to the threat of a tsunami that could produce strong currents dangerous to those in or near the water.&nbsp;</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p>Significant inundation is not expected for areas under an Advisory. Coastal zones may be at risk due to strong currents.&nbsp;</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p>Appropriate actions by local emergency management personnel may include closing beaches, and evacuating harbours and marinas.&nbsp;</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p><strong>Watch:</strong>&nbsp;</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p>The third highest level of tsunami alert. Watches are based on seismic information without confirmation that a destructive tsunami is underway.&nbsp;</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p>There is a potential threat to a zone under a Watch but communities have time to prepare. Emergency management personnel and coastal residents should prepare to take action in case the Watch is upgraded.&nbsp;</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p><strong>Cancellation:</strong>&nbsp;</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p>Cancels any previously issued tsunami messages. It is issued when there is no longer observed evidence of tsunami waves at tide gauge stations. Local conditions may differ from those at tide gauge stations and local authorities should determine the safety of coastlines.&nbsp;</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p>Once a cancellation has been issued for a tsunami event, the Ministry of Emergency Management and Climate Readiness will no longer issue tsunami messages.&nbsp;</p>
                    <!-- /wp:paragraph -->

