                    <h2 class="wp-block-heading is-style-alphagov-heading is-style-heading-callout">Emergency terms</h2>
                    <!-- /wp:heading -->

                    <!-- wp:heading {"level":3} -->
                    <h3 class="wp-block-heading">General</h3>
                    <!-- /wp:heading -->

                    <!-- wp:paragraph -->
                    <p><strong>BC Emergency Alert: </strong>BC Emergency Alerts are broadcast on TV, radio, and to cell phones to provide urgent public safety information to targeted areas during life-threatening emergencies. &nbsp;</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p><strong>Local State of Emergency:</strong> Declared by a local government when an emergency or disaster within its jurisdiction requires access to emergency powers provided under the <em>Emergency and Disaster Management Act</em>.</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p><strong>State of Provincial Emergency:</strong> Declared by the Government of British Columbia when an emergency or disaster within B.C. requires access to emergency powers provided under the <em>Emergency and Disaster Management Act</em>.</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p><strong>Evacuation Alert:</strong> Be ready to leave on short notice. If you leave before or during this alert, it’s called a voluntary evacuation.</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p><strong>Evacuation Order:</strong> You are at risk. Leave the area immediately.</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p><strong>Evacuation Rescinded:</strong> The risk to life and safety has now passed. It is safe to return home. Stay tuned for other possible evacuation alerts or orders.</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p><strong>Tactical Evacuation:</strong> Evacuations enacted very quickly, when a sudden threat to life requires immediate action and there is no time to prepare or issue written warnings. These types of evacuations are often coordinated by the local fire department, RCMP, or local police, with assistance from other agencies. Leave the area immediately, following the advice and direction of local authorities and emergency responders.<strong>Shelter-in-place:</strong> Anyone outside is at risk. Go inside and close and lock all windows and exterior doors.</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:heading {"level":3} -->
                    <h3 class="wp-block-heading">By emergency</h3>
                    <!-- /wp:heading -->

                    <!-- wp:paragraph -->
                    <p><strong>Tsunami</strong></p>
                    <!-- /wp:paragraph -->

                    <!-- wp:list -->
                    <ul class="wp-block-list"><!-- wp:list-item -->
                        <li><strong>Tsunami Warning:</strong> Flood wave possible. Full evacuation suggested.</li>
                        <!-- /wp:list-item -->

                        <!-- wp:list-item -->
                        <li><strong>Tsunami Advisory: </strong>Strong currents likely. Stay away from the shore.</li>
                        <!-- /wp:list-item -->

                        <!-- wp:list-item -->
                        <li><strong>Tsunami Watch:</strong> Danger level not yet known. Stay alert for more information.</li>
                        <!-- /wp:list-item -->

                        <!-- wp:list-item -->
                        <li><strong>Tsunami Information Statement:</strong> Minor waves at most. No action suggested.</li>
                        <!-- /wp:list-item -->

                        <!-- wp:list-item -->
                        <li><strong>Tsunami Cancellation:</strong> Tidal gauges show no wave activity. Confirm safety for local areas.</li>
                        <!-- /wp:list-item -->
                    </ul>
                    <!-- /wp:list -->

                    <!-- wp:paragraph -->
                    <p><strong>Heat Warning:</strong> Issued by Environment and Climate Change Canada when 2 or more consecutive days of daytime maximum temperatures are expected above set thresholds (variable by region).</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p><strong>Extreme Cold Warning:</strong> Issued by Environment and Climate Change Canada when any combination of wind speed and temperature create cold weather conditions that are dangerous to health and safety (variable by region).</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p><strong>Arctic Outflow Warning:</strong> For coastal areas of B.C., issued by Environment and Climate Change Canada when any combination of wind speed and temperature create cold weather conditions that are dangerous to health and safety.</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p><strong>Amber Alert:</strong> When a child abduction situation meets established criteria, an Amber Alert is issued to solicit the public’s help for the safe and prompt return of the abducted child.</p>
                    <!-- /wp:paragraph -->

                    <!-- wp:paragraph -->
                    <p><strong>Flood</strong></p>
                    <!-- /wp:paragraph -->

                    <!-- wp:list -->
                    <ul class="wp-block-list"><!-- wp:list-item -->
                        <li><strong>Flood Warning:</strong>&nbsp;River levels have exceeded the riverbank or will exceed the riverbank imminently, and flooding of areas adjacent to affected rivers will result.&nbsp;</li>
                        <!-- /wp:list-item -->

                        <!-- wp:list-item -->
                        <li><strong>Flood Watch:</strong>&nbsp;River levels are rising and will approach or may exceed the riverbank. Flooding of areas adjacent to affected rivers may occur.&nbsp;</li>
                        <!-- /wp:list-item -->

                        <!-- wp:list-item -->
                        <li><strong>High Streamflow Advisory:</strong>&nbsp;River levels are rising or are expected to rise rapidly, but no major flooding is expected. Minor flooding in low-lying areas is possible.&nbsp;</li>
                        <!-- /wp:list-item -->

                        <!-- wp:list-item -->
                        <li><strong>Freshet:</strong>&nbsp;Spring flooding due to snowmelt and rainfall.&nbsp;</li>
